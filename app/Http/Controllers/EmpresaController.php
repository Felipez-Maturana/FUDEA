    public function buscarsocio(Request $request)
    {
        if($request)
        {
            $query=trim($request->get('searchText'));
            $socio=DB::table('socios')
            ->select(DB::raw('estadoSuscripcion'))
            ->where('RUN','=',$query)
            ->get();
            return view('empresa.index',["searchText"=>$query,"socio"=>$socio]);

